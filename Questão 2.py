#Texto em Original#
texto = 'eu tenho um sonho. O sonho de ver meus filhos julgados por sua personalidade, não pela cor de sua pele.'

#Texto Original#
print('\n''Questão 2')
print('Texto Original: ',texto)

#Texto em Maiusculo#
print('\n''Questão 2 Letra A')
print('Texto todo em Maiusculo: ', texto.upper())


#Texto em Minusculo#
print('\n''Questão 2 Letra B')
print('Texto todo em Minusculo: ', texto.lower())


#Texto a Primeira Maiuscula#
print('\n''Questão 2 Letra C')
print('Texto todo em Primeira em Maiusculo: ', texto.title())
