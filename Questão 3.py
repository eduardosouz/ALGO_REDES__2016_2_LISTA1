#Numeros da questão#
n1 = 200
n2 = 250
n3 = 8
n4 = 7
n5 = 10
n6 = 800
n7 = 2

#Operações#
soma = (n1 + n2)
divisao = (n1 / n3)
mutiplicacao = (n1 * n4)
subtracao = (n6 - n5)
potencia = (n7 ** n5)

#Numeros a Ser Trabalhados#
print('Numeros = ',n1,',',n2,',',n3,',',n4,',',n5,',',n6,',',n7)

#Resolução dos Problemas#
print('\n''Resposta da letra A, Soma dos numeros',n1, '+',n2,'é igual:',soma)
print('\n''Resposta da letra B, Divisão dos numeros',n1, '/',n3,'é igual:',divisao)
print('\n''Resposta da letra C, Mutiplicação dos numeros',n1, 'x',n4,'é igual:', mutiplicacao)
print('\n''Resposta da letra D, Subtração dos numeros',n6, '-',n5,'é igual:', subtracao)
print('\n''Resposta da letra E, Potencia dos numeros de',n7, '^',n5,'é igual:', potencia)
